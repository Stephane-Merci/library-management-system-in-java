    private void FinesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinesActionPerformed
        // TODO add your handling code here:
    Connection connect;
    ResultSet dbrs;
    PreparedStatement ps;
    connect= JavaConnect.connectDb();
    Statement mystat;
        
    DateFormat dateFormat=new SimpleDateFormat("yyyy-MM-dd");
    Date date=new Date();
    String Date=dateFormat.format(date); //to print the current date
    try{
        String q = "select datelimit,fines,bookId,matricule from borrow WHERE userName ='"+this.username.getText()+"'" ;
        ps=connect.prepareStatement(q);
        dbrs =ps.executeQuery();
        while (dbrs.next()){
            Date datelim = dbrs.getDate(1);
            float fine = dbrs.getFloat(2);
            int bookId = dbrs.getInt(3);
            int matricule = dbrs.getInt(4);
            if( datelim.compareTo(date) < 0){
                //late
                if (fine == 0){
                    float fine1 = fine + 5000 ; 
                    String queryQ="UPDATE borrow SET fines = '"+fine1+"' WHERE matricule = '"+matricule+"' and bookId='"+bookId+"' " ; 
                    mystat=connect.createStatement();
                    mystat.executeUpdate(queryQ);
                }
            }
        }
    }catch( SQLException e){
        JOptionPane.showMessageDialog(null, "e");
    }
    fines f = new fines();
    f.setVisible(true);
    f.finesTable();     
    this.dispose();
        //brings you to the payfine page
        //calculate the fine and display in the pay fine interface
    }//GEN-LAST:event_FinesActionPerformed

