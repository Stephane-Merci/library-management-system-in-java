    public  void homeTable(){
        try {
            
            Connection connect;
            ResultSet rs;
            PreparedStatement ps;
            connect= JavaConnect.connectDb();
            
            var logquery="select * from books ";
            ps=connect.prepareStatement(logquery);
            rs =ps.executeQuery();      
            
            ResultSetMetaData rm = rs.getMetaData();
            int c = rm.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
              Vector v2 = new Vector();
              
              for(int i = 1; i<= c; i++){    
                 v2.add(rs.getString("bookId"));
                 v2.add(rs.getString("bookName"));
                 v2.add(rs.getString("author"));
                 v2.add(rs.getString("publisher"));
                 v2.add(rs.getString("availability"));
                 v2.add(rs.getString("quantity"));
              }
              df.addRow(v2);              
            }
        }catch (SQLException ex) {
               JOptionPane.showMessageDialog(null, "e"); 
        }
    };

